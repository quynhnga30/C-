using System;

namespace Variable {
    class Program {
        static void Main(string[] args) {
            int a = 6;
            int b = 2;
            Console.WriteLine("a + b = " + (a + b));
            Console.WriteLine("a - b = " + (a - b));
            Console.WriteLine("a * b = " + (a * b));
            Console.WriteLine("a / b = " + (a / b));
        }
    }
}